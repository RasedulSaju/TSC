    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        t = t_t.getText();
        name = t_name.getText();
        course = t_c.getText();
        id1 = tu.getText();
        passw = tp.getText();
        int i = JOptionPane.showConfirmDialog(rootPane, s, "registration confirmation", OK_CANCEL_OPTION);
        if (i == JOptionPane.OK_OPTION) {
            if (tp.getText().equals(tp1.getText())) {
                e = ""+2;
                data_store st = new data_store(e, name, course, t, id1, passw);
                st.Update();
                JOptionPane.showMessageDialog(rootPane, "registration successful");
                 setVisible(false);
                 new TeacherRegiForm().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(rootPane, "password didn't matched");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

